public class BitwiseOperator_21 {
    public static void main(String[] args) {
        System.out.println(4&5);
        System.out.println(4&3);
        System.out.println(4|5);
        System.out.println(4|3);
        System.out.println(4^5);
        System.out.println(4^3);
        System.out.println(6<<2);
        System.out.println(6>>2);
    }
}
